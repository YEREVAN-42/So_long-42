char	*ft_strchr(const char *s, int c)
{
	char	*ptr;
	char	b;

	ptr = (char *)s;
	b = c;
	while (*ptr && *ptr != b)
		ptr++;
	if (*ptr == b)
		return (ptr);
	return (0);
}
